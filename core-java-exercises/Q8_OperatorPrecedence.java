public class Q8_OperatorPrecedence {
    public static void main(String[] args) {
        int result = 10 + 5 * 2;
        int result2 = (10 + 5) * 2;
        int result3 = 100 / 5 + 6 * 2 - 3;
        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
    }
} 